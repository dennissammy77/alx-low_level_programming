#include "3-calc.h"

/**
 * op_add - returns sum of a and b.
 * @a: first num.
 * @b: second num.
 * Return: sum.
 */
int op_add(int a,int b)
{
	return (a + b);
}

/**
 * op_sub - returns dif of a and b.
 * @a:first num.
 * @b:second num.
 * Return: diff.
 */
int op_sub(int a,int b)
{
	return (a - b);
}

/**
 * op_mul - returns product of a and b.
 * @a:first num.
 * @b:second num.
 * Return: mul.
 */
int op_mul(int a,int b)
{
	return (a * b);
}

/**
 * op_div - returns div of a and b.
 * @a:first num.
 * @b:second num.
 * Return: result.
 */
int op_div(int a,int b)
{
	return (a / b);
}

/**
 * op_mod - returns mod of a and b.
 * @a:first num.
 * @b:second num.
 * Return: result.
 */
int op_mod(int a,int b)
{
	return (a % b);
}
